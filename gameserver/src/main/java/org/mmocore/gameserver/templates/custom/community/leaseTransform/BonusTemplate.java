public class BonusTemplate {
    private int str;
    private int _int;
    private int con;
    private int dex;
    private int wit;
    private int men;

    public int getStr() {
        return str;
    }

    public void setStr(int str) {
        this.str = str;
    }

    public int getInt() {
        return _int;
    }

    public void setInt(int _int) {
        this._int = _int;
    }

    public int getCon() {
        return con;
    }

    public void setCon(int con) {
        this.con = con;
    }

    public int getDex() {
        return dex;
    }

    public void setDex(int dex) {
        this.dex = dex;
    }

    public int getWit() {
        return wit;
    }

    public void setWit(int wit) {
        this.wit = wit;
    }

    public int getMen() {
        return men;
    }

    public void setMen(int men) {
        this.men = men;
    }
}
